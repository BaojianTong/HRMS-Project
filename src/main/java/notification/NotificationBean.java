package notification;

public class NotificationBean
{
    int empId, chatN, salaryN, leaveApplyN, leaveResponseN, complainN;

    public int getEmpId()
    {
        return empId;
    }
    public void setEmpId(int empId) {
        this.empId = empId;
    }

    public int getChatN()
    {
        return chatN;
    }
    public void setChatN(int chatN) {
        this.chatN = chatN;
    }

    public int getSalaryN()
    {
        return salaryN;
    }
    public void setSalaryN(int salaryN) {
        this.salaryN = salaryN;
    }

    public int getLeaveApplyN()
    {
        return leaveApplyN;
    }
    public void setLeaveApplyN(int leaveApplyN) {
        this.leaveApplyN = leaveApplyN;
    }

    public int getLeaveResponseN()
    {
        return leaveResponseN;
    }
    public void setLeaveResponseN(int leaveResponseN) {
        this.leaveResponseN = leaveResponseN;
    }

    public int getComplainN()
    {
        return complainN;
    }
    public void setComplainN(int complainN) {
        this.complainN = complainN;
    }
}
